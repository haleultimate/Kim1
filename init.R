load_stock_history_PCA <- function(stx_list.old) {
  if (exists("data.env") & !is.null(stx_list.old)) return(stx_list.old)
  Sys.setenv(TZ = "UTC")
  adjustment <- TRUE
  start_date <- "2006-01-01" 
  end_date <- "2010-01-01"
  getSymbols(Symbols = com.env$stx_list,
             env=data.env,
             src = "yahoo", 
             index.class = "POSIXct",
             from = start_date, 
             to = end_date, 
             adjust = adjustment)
  #calc_adjusted_HLOJRlD(com.env$stx_list)
  
  return(com.env$stx_list)
}

